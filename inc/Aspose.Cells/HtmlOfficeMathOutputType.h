#ifndef ASPOSE_CELLS_HTMLOFFICEMATHOUTPUTTYPE_H
#define ASPOSE_CELLS_HTMLOFFICEMATHOUTPUTTYPE_H

namespace Aspose { namespace Cells {

/// <summary>
/// Represents how export OfficeMath to HTML.
/// </summary>
enum class HtmlOfficeMathOutputType {
    /// <summary>
    /// Converts OfficeMath to HTML as image specified by &lt;img&gt; tag.
    /// </summary>
    Image = 0,
    /// <summary>
    /// Converts OfficeMath to HTML using MathML.
    /// </summary>
    MathML = 1,
};

} }

#endif
